const month = "march"

switch (month) {
    case "January":
        console.log("January");
         break;
    case "February":
        console.log("February");
         break;
    case "March":
        console.log("March");
         break;
    case "April":
        console.log("April");
         break;

        default:
            console.log("defalut case match");
            
            break;
}